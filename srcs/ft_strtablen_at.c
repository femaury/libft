#include "libft.h"

size_t	ft_strtablen_at(char **s, int i, int j)
{
	size_t	x;

	x = 0;
	while (s[i][0])
	{
		j = 0;
		while (s[i][j])
		{
			j++;
			x++;
		}
		i++;
	}
	return (x);
}
